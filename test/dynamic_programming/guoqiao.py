'''
    在一个夜黑风高的晚上，有n（n <= 50）个小朋友在桥的这边，现在他们需要过桥，但是由于桥很窄，每次只允许不大于两人通过，
    他们只有一个手电筒，所以每次过桥的两个人需要把手电筒带回来，i号小朋友过桥的时间为T[i]，两个人过桥的总时间为二者中时
    间长者。问所有小朋友过桥的总时间最短是多少
'''

time = [1, 2, 5, 10, 10]

opt = [0] * len(time)
opt[0] = 0
opt[1] = time[1]

i = 2
while i < len(time):
    opt[i] = min(opt[i - 1] + time[0] + time[i], opt[i - 2] + time[0] + time[i] + 2 * time[1])
    i += 1

print(opt[-1])
